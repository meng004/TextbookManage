using System;
using System.Collections.Generic;

namespace TextbookManage.Domain.Models
{
    public class Subscription : AggregateRoot
    {
        public Subscription()
        {
            Declarations = new List<Declaration>();
        }

        #region 属性

        /// <summary>
        /// 订单ID
        /// </summary>
        public Guid SubscriptionId { get; set; }
        /// <summary>
        /// 书商ID
        /// </summary>
        public Guid Bookseller_Id { get; set; }
        /// <summary>
        /// 教材ID
        /// </summary>
        public Guid Textbook_Id { get; set; }
        /// <summary>
        /// 回告ID
        /// </summary>
        public int? Feedback_Id { get; set; }
        /// <summary>
        /// 学年学期
        /// </summary>
        public string Term { get; set; }
        /// <summary>
        /// 计划数量
        /// </summary>
        public int PlanCount { get; set; }
        /// <summary>
        /// 上抛数量
        /// </summary>
        public int SpareCount { get; set; }
        /// <summary>
        /// 征订日期
        /// </summary>
        public System.DateTime SubscriptionDate { get; set; }
        /// <summary>
        /// 书商
        /// </summary>
        public virtual Bookseller Bookseller { get; set; }
        /// <summary>
        /// 回告
        /// </summary>
        public virtual Feedback Feedback { get; set; }
        /// <summary>
        /// 教材
        /// </summary>
        public virtual Textbook Textbook { get; set; }
        /// <summary>
        /// 用书申报
        /// </summary>
        public virtual ICollection<Declaration> Declarations { get; set; }
        #endregion

        #region 业务规则

        /// <summary>
        /// 订单的回告状态
        /// </summary>
        /// <returns></returns>
        public FeedbackState FeedbackState
        {
            get
            {
                if (Feedback == null || Feedback.ApprovalState != ApprovalState.终审通过)
                {
                    return FeedbackState.征订中;
                }
                else
                {
                    return Feedback.FeedbackState;
                }
            }
        }
        #endregion

    }
}
